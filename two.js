// Convert Kilometers to Miles

// 1 kilometer is equal to 0.621371 miles.

// convert kilometers to miles, you can use the formula:
// miles = kilometers * 0.621371
function kilometertomiles(num){
    return num*0.621371;
}
console.log(kilometertomiles(2.2));

